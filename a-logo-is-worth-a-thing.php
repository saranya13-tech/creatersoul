                                <h3>Characterstics of a logo</h3>
                                <p>It should be simple, must be single, simple visual image, Hence customer can relate it with your company. Complicated structured logos are difficult to understand the purpose and will not serve the need of the company. So, it is advised to keep logo simple in shape.</p>
