									<h1 class="entry-title">Colours ain’t just visual,<br class="hide-mobile">
									it’s emotional</h1>
									<p>Colours in design play a pivotal role in evoking emotional and behavioural responses of users and customers!</p>
									<p> Ever stopped by while strolling to read a sign board with yellow background or got excited by that familiar sales discount in red? We all have been unwittingly trained to attach certain emotions, trends and assign meaning to colours. And mistake not, there’s an entire science behind colour psychology and colour theory. That’s why it’s important to understand the psychological effects colors might have on an average person as well as the fundamentals of color theory and the meanings of colors. Indeed design is as much a science as it is an art!</p>
									<h3>What is Colour Psychology?</h3>
									<p>Colours influences perceptions that are not obvious, from the taste of food we eat to our confidence level based on our outfit colour. Colour psychology is the study of hues as a determinant of human behavior which itself is a branch of much broader behavioural psychology.</p>
									<h3>How colours can help intentional design</h3>
									<p>Seeing red. Feeling blue. Green with envy. These famous idioms speak for themselves how popular culture have identified and used colour associations to our emotions. </p>
									<p>Designers well versed in their game know too well about the importance of colours and make good use of them - not just to make their designs visually appealing, but in preparing colour palettes and carefully placing purposefully coloured elements in their layout to invoke desired user response and action. Various case studies have shown that adjusting colour and other elements have a positive effect on website conversions as high as 24%. </p>
									<p>Colours when used craftily without going overboard can make phenomenal changes in various areas of design including : </p>
									<ul class="list-small">
										<li>Websites</li>
										<li>Company logos</li>
										<li>Branding</li>
										<li>Landingpages</li>
										<li>Restaurantmenubars</li>
										<li>Email marketing</li>
										<li>Social media posts and cover photos  Productdesign</li>
										<li>Videos</li>
									</ul>
									<h3>What colours do I use?</h3>
									<p>To convey the right tone, message and call users to make the expected action, designers need to understand what colours mean and what reaction they evoke. Though design is all about innovations & breaking conventions (intentionally and with good use), certain rules of thumb can be helpful to be kept in mind while designing anything to optimise intended human response.</p> <img src="images/colours-aint-just-visual-it-is-emotional.png" alt="colours-aint-just-visual-it-is-emotional">
									<h3>Red</h3>
									<p>Red usually associates with passionate, strong, and aggressive feelings.In design, the use of red colour is an effective way to draw users’ attention, but it’s always important to use red sparingly to avoid negative reactions.</p>
									<h3>Green</h3>
									<p>Often called the colour of nature, balance, and harmony, green brings calming and renewing feelings. Also, it is a sign of growth and inexperience - and hence has very good use case in colouring action buttons, such as subscriptions or sales, in green.</p>
									<h3>Blue</h3>
									<p>It often represents corporate images since blue evokes a sense of trust. It usually shows reliability, make users feel calm and safe. It’s a very good colour for backgrounds when not overdone.</p>
									<h3>Yellow</h3>
									<p>Yellow is thought to be the easiest colour to visibly see and is the colour of happiness which symbolizes sunlight, joy, and warmth. Also, yellow is often associated with hazard signs and hence one has to be vigilant while using yellow for background colours.</p>
									<h3>Black</h3>
									<p>Black is a versatile colour with association to a great number of meanings. The response a black element evokes largely depends not on itself, but on how you employ it and more importantly which colours go with it. Black is ideal for the background and designers often use it to set contrasts.</p>
									<h3>White</h3>
									<p>White denotes purity and innocence. White is commonly used as the background colour especially to make content more readable and for the design not to feel too heavy. </p>
									<p>Colours speak directly to your sub-concious</p>
									<p>Most user engagements and behaviours are seldom rational and well thought out - they’re often quick instincts and gut feelings based on the emotional response evoked. So next time you set out to design or to assign a design assignment for your company, product or service make sure you get the needed response from users by intelligently using colours!</p>
