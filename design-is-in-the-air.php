									<h1 class="entry-title">Design is in the air</h1>
									<p> Design is perhaps the most underrated and the most overlooked entity in our life. It’s that one thing that has so much unseen potential in shaping our choices and behaviours. It influences how we chose a product, service or our favourite brand of clothing or food.</p>
									<p>Just recollect your morning and you’ll be amazed at how much influence design has had in so little a time as you get up, get ready for your day and head out to your office: </p>
									<ul>
										<li>Your choice of toothbrush is most likely because you liked its design when you saw it in the supermarket. </li>
										<li> Your favourite shirt is that blue one because a fashion designer has put thought into it. </li>
										<li>You picked your dishes and silvers most certainly because you liked the way they look. </li>
										<li>Your favourite breakfast spot or that cafe that you like to work in has a lot of design thought process right from the table, chairs to the lighting and ambience to the menu even to the way they decide to dress and present your favourite drink. </li>
										<li>The font and colours of road signs you see are designed with a strong purpose in mind. In other words design is inevitable in our lives. The way we interact with our world is in itself designed in multiple ways. Be it the physical objects we touch or the user interface on our touch screens - design not just there to attract or make us feel good, but to help us, the users, form associations that are so etched in our minds that we recognize familiar logos and other design elements in an instant and a plethora of features rush into our mind. The blue ‘f’ is recognizable by even toddlers in this age, the red, yellow, green and blue combination used in multiple contexts are just enough to make us connect to the Google brand. Remember the infamous logo game? It works so well because great design is so effective in communicating more than a thousand words in an icon!</li>
									</ul> <img src="images/design-is-in-the-air.png" alt="">
									<p>Design is not just about the visual elements, it’s also about the experience delivered to a user or customer. Ever visited an Apple store? From the very onset of entry to store, the customer is pampered and delivered with just enough information to entice you and make you feel valued for your time you spend with the service. This boosts your self esteem and you’re more likely to buy their product upgrade or avail their services against other alternatives. Similarly, a well designed website not just looks or feels good, but makes you feel valued with the way you interact with different elements on the site. </p>
									<p>Design isn’t just about the way it looks,but more about the way it feels - or rather what feelings the colours, the shapes and the layout arouses in you. A great design is an intentional act, lot of thought goes into thinking and planning about the emotions that the design elicits. Talking about emotions, design plays a pivotal role in determining the trustworthiness and credibility of a brand or company. According to a survey by Stanford Persuasive Technology Lab, almost half of the consumers evaluated stated that design was the number one criteria when evaluating a website's credibility. </p>
									 
									<article class="quote">
										<h4>Design really is everywhere, so take inspiration from your surroundings</h4> <span class="author">Emily Stevens</span> </article>
									<p> Purposeful design with the user in mind does not intimidate you, but makes you feel ‘safe’,‘trusted’, ‘beautiful’ and makes you want to be ‘proud’ about owning or using the product or service. In fact, design is one are where mindful thought process pays back the most. Next time you head out of your house or even while sitting in your couch, if your mindful about it - you can never stop admiring great design, cause design is everywhere!</p>
